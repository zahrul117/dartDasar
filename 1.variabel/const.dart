void main() {
  // Menggunakan const untuk nilai tetap yang diketahui saat compile-time
  const phi = 3.14; // Nilai phi bersifat tetap, tidak bisa diubah
  const tahun = 2025; // Tahun juga ditetapkan sebagai konstanta

  // tahun = 2030;

  // Menampilkan nilai-nilai konstanta ke konsol
  print('Nilai phi adalah $phi'); // Output: Nilai phi adalah 3.14
  print('Tahun sekarang $tahun'); // Output: Tahun sekarang 2025
}
